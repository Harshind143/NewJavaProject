package com.javaloops;

public class forloopoddevendemo {

    public static void main(String[] args) {

        for (int i = 1; i < 10; i++) {

            if(i%2==0){
                System.out.println("Number is Even "+ i);

            }else{
                System.out.println("Number is odd "+ i);
            }

        }
    }
}
